    public void limpiarTabla(javax.swing.JTable tabla){
        DefaultTableModel dtm = (DefaultTableModel) tabla.getModel();
        for(int i=0;i < dtm.getRowCount();i++){
            dtm.removeRow(i);
        }
    }
    
    
